//  The ternary operator is a shorthand way of writing an if-else statement in a single line. It is 
//  also known as the conditional operator. 
//  the syntx of ternary operator is:
//  condition ? expression_if_true : expression_if_false;


// javascript Example:

const number=7;

const result=number % 2 === 0 ? "even" : "odd";
console.log(`The number ${number} is ${result}.`)



